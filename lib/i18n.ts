interface I18nContextValue {
  locale: Locale
  setLocale: (locale: Locale) => void
  t: (key: string, fallback?: string) => string
}

const I18nContext = createContext<I18nContextValue | undefined>(undefined)

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [locale, setLocale] = useState<Locale>("en")

  useEffect(() => {
    const stored = typeof window !== "undefined" ? window.localStorage.getItem("locale") : null
    if (stored === "en" || stored === "fr") {
      setLocale(stored)
      return
    }
    if (typeof navigator !== "undefined") {
      const lang = navigator.language?.toLowerCase()
      if (lang.startsWith("en")) setLocale("en")
      if (lang.startsWith("fr")) setLocale("fr")
    }
  }, [])

  const value = useMemo<I18nContextValue>(() => {
    const t = (key: string, fallback?: string) => {
      const msg = messages[locale]?.[key]
      return msg ?? fallback ?? key
    }
    const set = (loc: Locale) => {
      setLocale(loc)
      if (typeof window !== "undefined") {
        window.localStorage.setItem("locale", loc)
      }
    }
    return { locale, setLocale: set, t }
  }, [locale])

  return React.createElement(I18nContext.Provider, { value }, children)
}

export function useI18n() {
  const ctx = useContext(I18nContext)
  if (!ctx) {
    throw new Error("useI18n must be used within I18nProvider")
  }
  return ctx
}
